let person = {
  name: 'Chizoba',
  age: 30,
  location: 'abj'
}
console.log(person)

// for object
person = Object.create(null)
person.name = 'Chizoba'
person.age = '30'
person.location = 'abj'

// for dot notation or bracket
console.log(person.name)
console.log(person.age)
console.log(person.location)
